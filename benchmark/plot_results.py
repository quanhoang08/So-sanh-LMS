#!/usr/bin/env python3
"""
Plot benchmark charts from CSV files generated by export-benchmark.ps1.

Expected layout:
  benchmark/results/run-YYYYMMDD-HHMMSS/
    - benchmark-summary.csv
    - benchmark-all-runs.csv
    - *-cpu-samples.csv
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import List

import matplotlib.pyplot as plt
import pandas as pd


def save_current_figure(out_dir: Path, stem: str, formats: List[str]) -> List[Path]:
    saved: List[Path] = []
    for fmt in formats:
        out = out_dir / f"{stem}.{fmt}"
        plt.savefig(out, dpi=150, format=fmt)
        saved.append(out)
    return saved


def find_latest_run(results_dir: Path) -> Path:
    run_dirs = sorted([p for p in results_dir.glob("run-*") if p.is_dir()])
    if not run_dirs:
        # Fallback: allow legacy layout where files are directly in results/
        has_csv_or_json = any(results_dir.glob("*.csv")) or any(results_dir.glob("*.json"))
        if has_csv_or_json:
            return results_dir
        raise FileNotFoundError(
            f"Khong tim thay folder run-* trong: {results_dir}. "
            "Hay chay export-benchmark.ps1 truoc."
        )
    return run_dirs[-1]


def normalize_architecture(name: str) -> str:
    s = (name or "").strip().lower()
    if "micro" in s:
        return "Microservices"
    return "Monolith"


def plot_summary(summary_csv: Path, out_dir: Path, formats: List[str]) -> List[Path]:
    df = pd.read_csv(summary_csv)
    if "architecture" not in df.columns:
        raise ValueError(f"{summary_csv} khong co cot 'architecture'.")

    df["architecture"] = df["architecture"].apply(normalize_architecture)
    df = df.set_index("architecture")

    metrics = [
        (["rps", "rps_avg"], "Throughput (RPS)", "rps"),
        (["avg_ms", "avg_ms_avg"], "Average Latency (ms)", "latency_avg_ms"),
        (["p95_ms", "p95_ms_avg"], "P95 Latency (ms)", "latency_p95_ms"),
        (["p99_ms", "p99_ms_avg"], "P99 Latency (ms)", "latency_p99_ms"),
        (["avg_cpu_percent", "cpu_percent_avg"], "Average CPU (%)", "avg_cpu_percent"),
    ]

    saved: List[Path] = []
    for candidates, title, stem in metrics:
        col = next((c for c in candidates if c in df.columns), None)
        if col is None:
            continue
        # Keep a consistent y-axis label regardless of source column alias.
        y_label = candidates[0]
        ax = df[col].plot(kind="bar", rot=0, figsize=(7, 4), color=["#4C78A8", "#F58518"])
        ax.set_title(title)
        ax.set_xlabel("Architecture")
        ax.set_ylabel(y_label)
        ax.grid(axis="y", linestyle="--", alpha=0.35)
        plt.tight_layout()
        saved.extend(save_current_figure(out_dir, stem, formats))
        plt.close()

    return saved


def plot_all_runs(all_runs_csv: Path, out_dir: Path, formats: List[str]) -> List[Path]:
    df = pd.read_csv(all_runs_csv)
    needed = {"architecture", "run"}
    if not needed.issubset(df.columns):
        raise ValueError(f"{all_runs_csv} can cac cot: {sorted(needed)}")

    df["architecture"] = df["architecture"].apply(normalize_architecture)
    df["run"] = pd.to_numeric(df["run"], errors="coerce")
    df = df.sort_values(["architecture", "run"])

    metrics = [
        ("rps", "RPS per Run", "runs_rps"),
        ("avg_ms", "Avg Latency per Run (ms)", "runs_avg_ms"),
        ("p95_ms", "P95 Latency per Run (ms)", "runs_p95_ms"),
        ("avg_cpu_percent", "Avg CPU per Run (%)", "runs_avg_cpu_percent"),
    ]

    saved: List[Path] = []
    for col, title, stem in metrics:
        if col not in df.columns:
            continue
        pivot = df.pivot(index="run", columns="architecture", values=col)
        ax = pivot.plot(marker="o", figsize=(8, 4))
        ax.set_title(title)
        ax.set_xlabel("Run")
        ax.set_ylabel(col)
        ax.grid(True, linestyle="--", alpha=0.35)
        plt.tight_layout()
        saved.extend(save_current_figure(out_dir, stem, formats))
        plt.close()

    return saved


def infer_container_col(df: pd.DataFrame) -> str | None:
    candidates = ["container", "container_name", "name", "service"]
    for c in candidates:
        if c in df.columns:
            return c
    return None


def infer_cpu_col(df: pd.DataFrame) -> str | None:
    candidates = ["cpu_percent", "cpu", "cpu_pct", "cpu_usage_percent"]
    for c in candidates:
        if c in df.columns:
            return c
    return None


def plot_cpu_samples(run_dir: Path, out_dir: Path, formats: List[str]) -> List[Path]:
    saved: List[Path] = []
    cpu_files = sorted(run_dir.glob("*-cpu-samples.csv"))

    for cpu_file in cpu_files:
        df = pd.read_csv(cpu_file)
        container_col = infer_container_col(df)
        cpu_col = infer_cpu_col(df)
        if not container_col or not cpu_col:
            continue

        # Compute average CPU per container for each architecture file.
        grouped = (
            df.groupby(container_col, dropna=False)[cpu_col]
            .mean()
            .sort_values(ascending=False)
            .head(12)
        )
        if grouped.empty:
            continue

        ax = grouped.plot(kind="bar", figsize=(10, 4), color="#72B7B2")
        ax.set_title(f"Avg CPU by Container - {cpu_file.stem}")
        ax.set_xlabel("Container")
        ax.set_ylabel(cpu_col)
        ax.grid(axis="y", linestyle="--", alpha=0.35)
        plt.tight_layout()
        saved.extend(save_current_figure(out_dir, f"{cpu_file.stem}_by_container", formats))
        plt.close()

    return saved


def main() -> None:
    parser = argparse.ArgumentParser(description="Plot benchmark charts from CSV files.")
    parser.add_argument(
        "--results-dir",
        type=Path,
        default=Path("results"),
        help="Thu muc results goc (mac dinh: ./results)",
    )
    parser.add_argument(
        "--run-dir",
        type=Path,
        default=None,
        help="Chi dinh truc tiep folder run-YYYYMMDD-HHMMSS. Neu bo qua se lay run moi nhat.",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        help="Thu muc xuat chart. Mac dinh: <run-dir>/charts",
    )
    parser.add_argument(
        "--formats",
        type=str,
        default="png",
        help="Dinh dang anh, vi du: png hoặc png,jpg (mac dinh: png)",
    )
    args = parser.parse_args()
    formats = [f.strip().lower() for f in args.formats.split(",") if f.strip()]
    allowed = {"png", "jpg", "jpeg"}
    invalid = [f for f in formats if f not in allowed]
    if invalid:
        raise ValueError(f"Dinh dang khong ho tro: {invalid}. Chi ho tro: {sorted(allowed)}")
    if not formats:
        formats = ["png"]

    run_dir = args.run_dir if args.run_dir else find_latest_run(args.results_dir)
    out_dir = args.output_dir if args.output_dir else (run_dir / "charts")
    out_dir.mkdir(parents=True, exist_ok=True)

    saved: List[Path] = []

    summary_csv = run_dir / "benchmark-summary.csv"
    all_runs_csv = run_dir / "benchmark-all-runs.csv"

    if summary_csv.exists():
        saved.extend(plot_summary(summary_csv, out_dir, formats))
    else:
        print(f"[WARN] Khong tim thay {summary_csv.name}")

    if all_runs_csv.exists():
        saved.extend(plot_all_runs(all_runs_csv, out_dir, formats))
    else:
        print(f"[WARN] Khong tim thay {all_runs_csv.name}")

    saved.extend(plot_cpu_samples(run_dir, out_dir, formats))

    if saved:
        print("Da tao cac chart:")
        for p in saved:
            print(f" - {p}")
    else:
        print(
            "Chua tao duoc chart nao. Kiem tra lai file CSV trong run dir "
            f"({run_dir}) co dung dinh dang khong."
        )


if __name__ == "__main__":
    main()
